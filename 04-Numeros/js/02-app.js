const numero1 = 30;
const numero2 = 20;

let resultado;

// Suma
resultado = numero1 + numero2;
console.log(resultado);

// Resta
resultado = numero1 - numero2;
console.log(resultado);

// Multiplicacion
resultado = numero1 * numero2;
console.log(resultado);

// division
resultado = numero1 / numero2;
console.log(resultado);

// modulo
resultado = numero1 % numero2;
console.log(resultado);

console.log(resultado);
